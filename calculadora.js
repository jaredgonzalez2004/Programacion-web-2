//Se solicita dos numeros cualquiera para realizar las operaciones matematicas
let n1 = parseInt (prompt("Primer numero: "));
let n2 = parseInt (prompt("Segundo numero: "));

//Se realian las operaciones basicas con los dos numeros que se solicitaron anteriormente 
let s = n1 + n2;    //suma
let r = n1 - n2;  //resta
let mt = n1 * n2;  //muliplicacion
let d = n1 / n2;   //division
let rs = n1 % n2;  //residuo

//Se imprimen los resultados de las operaciones en la consola
console.log("Suma: " + s);
console.log("Resta: " + r);
console.log("Multiplicacion: " + m);
console.log("Division: " + d);
console.log("Residuo: " + rs);


